/**
 * Generate an Unsplash image URL for a recipe based on its title
 * Uses curated high-quality food images matched to recipe names
 */
export function getRecipeImageUrl(recipeTitle: string, mealType?: string, imageSearchTerm?: string): string {
  // Use the specific search term if provided (from AI analysis)
  const searchTitle = imageSearchTerm || recipeTitle;
  return getSpecificRecipeImage(searchTitle, mealType);
}

