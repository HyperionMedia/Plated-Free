    'potato', 'fries', 'mash',
    // Vegetables
    'avocado', 'tomato', 'broccoli', 'spinach', 'kale', 'lettuce',
    'mushroom', 'pepper', 'onion', 'garlic', 'carrot',
    // Dairy
    'cheese', 'yogurt', 'milk', 'butter', 'cream',
    // Plant-based
    'tofu', 'tempeh', 'beans', 'lentils', 'chickpeas', 'hummus',
    // Fruits
    'apple', 'banana', 'berry', 'berries', 'strawberry', 'blueberry',
    'orange', 'mango', 'peach', 'pear',
